import { useEffect, useMemo, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogFooter, DialogDescription } from '@/components/ui/dialog';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Badge } from '@/components/ui/badge';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue, SelectSeparator } from '@/components/ui/select';
import { Popover, PopoverContent, PopoverTrigger } from '@/components/ui/popover';
import { Command, CommandEmpty, CommandGroup, CommandInput, CommandItem, CommandList } from '@/components/ui/command';
import { Check, ChevronDown, Wrench, X, Plus, PlusCircle } from 'lucide-react';
import { cn } from '@/lib/utils';
import {
  applications, modules as allModules, features as allFeatures,
  releases as allReleases, processes as allProcesses, labelVocabulary,
  testCases as testCaseStore,
  createModule, createFeature, createRelease, createProcess, addLabelToVocabulary,
  generateCaseNumber as buildCaseNumber,
} from '@/data/mock';
import { useWorkspace } from '@/contexts/WorkspaceContext';
import type { Module, TestCase } from '@/types';
import { toast } from '@/hooks/use-toast';

interface NewTestCaseDialogProps {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  appId: string;
  /** Optional pre-fill from current Library scope/filters. */
  prefill?: {
    moduleId?: string;
    featureId?: string;
    releaseIds?: string[];
    processIds?: string[];  
    labels?: string[];
  };
}

type StatusValue = 'valid' | 'draft' | 'archived';
const STATUS_OPTIONS: Array<{ id: StatusValue; label: string }> = [
  { id: 'draft', label: 'Draft' },
  { id: 'valid', label: 'Published' },
  { id: 'archived', label: 'Retired' },
];

// Case numbers are now generated by the shared deterministic helper in
// `src/data/mock.ts` (`generateCaseNumber`), which uses a per-app sequence
// seeded from existing cases so duplicates can never collide.

const NEW_SENTINEL = '__NEW__';

export function NewTestCaseDialog({ open, onOpenChange, appId, prefill }: NewTestCaseDialogProps) {
  const navigate = useNavigate();
  const { isMasterRepo, bumpRepoVersion } = useWorkspace();
  const app = applications.find(a => a.id === appId);

  // Local "version" counter to force option lists to refresh after inline-create.
  const [taxVersion, setTaxVersion] = useState(0);
  const refreshTaxonomy = () => { setTaxVersion(v => v + 1); bumpRepoVersion(); };

  const [apiModules, setApiModules] = useState<Module[]>([]);
  const [apiModulesProductId, setApiModulesProductId] = useState<string>('');
  const [isLoadingModules, setIsLoadingModules] = useState(false);

  const appModules = useMemo(() => {
    const sourceModules = apiModules.length ? apiModules : allModules;
    const seen = new Set<string>();
    return sourceModules.filter(m => m.applicationId === appId && (seen.has(m.name) ? false : (seen.add(m.name), true)));
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [appId, apiModules.length, taxVersion]);

  const [name, setName] = useState('');
  const [moduleId, setModuleId] = useState<string>('');
  const [featureId, setFeatureId] = useState<string>('');
  const [role, setRole] = useState('');
  const [releaseIds, setReleaseIds] = useState<string[]>([]);
  const [processIds, setProcessIds] = useState<string[]>([]);
  const [labels, setLabels] = useState<string[]>([]);
  const [status, setStatus] = useState<StatusValue>('draft');
  const [type, setType] = useState<'standard' | 'customized'>('standard');

  // Inline-create UI state
  const [newModuleDraft, setNewModuleDraft] = useState<string | null>(null);
  const [newFeatureDraft, setNewFeatureDraft] = useState<string | null>(null);

  // Reset/prefill on open
  useEffect(() => {
    if (!open) return;
    setName('');
    setRole('');
    setStatus('draft');
    setType('standard');
    setReleaseIds(prefill?.releaseIds ?? []);
    setProcessIds(prefill?.processIds ?? []);
    setLabels(prefill?.labels ?? []);
    setNewModuleDraft(null);
    setNewFeatureDraft(null);

    let mId = prefill?.moduleId ?? '';
    let fId = prefill?.featureId ?? '';
    if (!mId && appModules.length) mId = appModules[0].id;
    setModuleId(mId);

    if (mId) {
      const sameNameIds = allModules
        .filter(m => m.name === allModules.find(x => x.id === mId)?.name && m.applicationId === appId)
        .map(m => m.id);
      const feats = allFeatures.filter(f => sameNameIds.includes(f.moduleId));
      setFeatureId(fId && feats.find(x => x.id === fId) ? fId : (feats[0]?.id ?? ''));
    }
  }, [open, prefill, appModules, appId]);

  const moduleFeatures = useMemo(() => {
    if (!moduleId) return [];
    const target = allModules.find(m => m.id === moduleId);
    if (!target) return [];
    const sameNameIds = allModules
      .filter(m => m.name === target.name && m.applicationId === appId)
      .map(m => m.id);
    return allFeatures.filter(f => sameNameIds.includes(f.moduleId));
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [moduleId, appId, taxVersion]);

  // Keep feature valid when module changes
  useEffect(() => {
    if (moduleFeatures.length === 0) {
      setFeatureId('');
    } else if (!moduleFeatures.find(f => f.id === featureId)) {
      setFeatureId(moduleFeatures[0].id);
    }
  }, [moduleFeatures, featureId]);

  const appReleases = useMemo(
    () => allReleases.filter(r => r.applicationId === appId),
    // eslint-disable-next-line react-hooks/exhaustive-deps
    [appId, taxVersion],
  );
  const appProcesses = useMemo(
    () => allProcesses.filter(p => !p.applicationId || p.applicationId === appId),
    // eslint-disable-next-line react-hooks/exhaustive-deps
    [appId, taxVersion],
  );

  const canSubmit = name.trim().length > 0 && moduleId && featureId;

 const fetchModulesForApp = async (
  productId: string
) => {
  setIsLoadingModules(true);

  try {
    const url = `http://localhost:8001/api/v1/modules?product_id=${productId}`;

    const response = await fetch(url);

    if (!response.ok) {
      throw new Error(
        `Fetch failed with status ${response.status}`
      );
    }

    const raw = await response.json();

    const list = Array.isArray(raw)
      ? raw
      : raw.items ?? [];

    const data: Module[] = list
      .map((item: any) => ({
        id: item.id,
        name: item.name,
        abbreviation: item.abbreviation,
        description: item.description,
        applicationId: productId,
      }))
      .filter((m) => m.id && m.name);

    setApiModules(data);
    setApiModulesProductId(productId);
  } catch (error) {
    const message =
      error instanceof Error
        ? error.message
        : "Unknown error";

    toast({
      title: "Failed to load modules",
      description: message,
      variant: "destructive",
    });
  } finally {
    setIsLoadingModules(false);
  }
};
  const handleModuleChange = (value: string) => {
    if (value === NEW_SENTINEL) {
      setNewModuleDraft('');
      setModuleId('');
      setFeatureId('');
      return;
    }

    setModuleId(value);
    setFeatureId('');
  };

  // ---- inline-create handlers ----
const handleCreateModule = async () => {
  const v = (newModuleDraft ?? "").trim();

  if (!v) return;

  if (
    apiModules.some(
      (m) => m.name.toLowerCase() === v.toLowerCase()
    )
  ) {
    toast({
      title: "Module already exists",
      description: v,
      variant: "destructive",
    });
    return;
  }

  const productId =
    "1eef4151-3303-4e6b-b784-0f43a24e2b00";

  try {
    const payload = {
      product_id: productId,
      name: v,
      abbreviation: v
        .slice(0, 3)
        .toUpperCase(),
      description: `${v} module`,
    };

    const response = await fetch(
      "http://localhost:8001/api/v1/modules",
      {
        method: "POST",
        headers: {
          "Content-Type":
            "application/json",
        },
        body: JSON.stringify(payload),
      }
    );

    if (!response.ok) {
      throw new Error(
        `Create failed: ${response.status}`
      );
    }

    const data = await response.json();

    // ADD newly created module to dropdown state
    setApiModules((prev) => [
      ...prev,
      {
        id: data.id,
        name: data.name,
        abbreviation:
          data.abbreviation,
        description:
          data.description,
        applicationId:
          productId,
      },
    ]);

    // Auto select created module
    setModuleId(data.id);

    setFeatureId("");
    setNewModuleDraft(null);

    toast({
      title: "Module created",
      description: data.name,
    });
  } catch (error) {
    const message =
      error instanceof Error
        ? error.message
        : "Unknown error";

    toast({
      title:
        "Failed to create module",
      description: message,
      variant: "destructive",
    });
  }
};
  const handleCreateFeature = () => {
    const v = (newFeatureDraft ?? '').trim();
    if (!v || !moduleId) return;
    if (moduleFeatures.some(f => f.name.toLowerCase() === v.toLowerCase())) {
      toast({ title: 'Feature already exists', description: v, variant: 'destructive' });
      return;
    }
    const f = createFeature({ name: v, moduleId });
    refreshTaxonomy();
    setFeatureId(f.id);
    setNewFeatureDraft(null);
    toast({ title: 'Feature created', description: f.name });
  };

  const handleCreateRelease = (name: string, version: string) => {
    if (appReleases.some(r => r.name.toLowerCase() === name.toLowerCase())) {
      toast({ title: 'Release already exists', description: name, variant: 'destructive' });
      return null;
    }
    const r = createRelease({ name, version, applicationId: appId });
    refreshTaxonomy();
    setReleaseIds(prev => [...prev, r.id]);
    toast({ title: 'Release created', description: r.name });
    return r;
  };

  const handleCreateProcess = (name: string) => {
    if (appProcesses.some(p => p.name.toLowerCase() === name.toLowerCase())) {
      toast({ title: 'Process already exists', description: name, variant: 'destructive' });
      return null;
    }
    const p = createProcess({ name, applicationId: appId });
    refreshTaxonomy();
    setProcessIds(prev => [...prev, p.id]);
    toast({ title: 'Process created', description: p.name });
    return p;
  };

  const handleOpenWorkbench = () => {
    if (!canSubmit || !app) return;
    const newId = `tc-new-${Date.now()}`;
    const primaryReleaseId = releaseIds[0] ?? (appReleases[0]?.id ?? 'rel-1');
    const newCase: TestCase = {
      id: newId,
      caseNumber: buildCaseNumber(app.id, moduleId),
      testCaseName: name.trim(),
      featureId,
      moduleId,
      releaseId: primaryReleaseId,
      releaseIds: releaseIds.length ? releaseIds : [primaryReleaseId],
      processIds,
      labels,
      role: role.trim() || 'Tester',
      description: '',
      expectedResult: '',
      dependency: undefined,
      type,
      status,
      order: 999,
      version: 1,
      createdAt: new Date().toISOString().slice(0, 10),
      updatedAt: new Date().toISOString().slice(0, 10),
    };
    testCaseStore.unshift(newCase);
    bumpRepoVersion();
    onOpenChange(false);
    toast({ title: 'Test case created', description: `${newCase.caseNumber} — opening Workbench` });
    navigate(`/test-case/${newId}?wb=1`);
  };

  if (!app) return null;
  
  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="max-w-xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle>New test case</DialogTitle>
          <DialogDescription className="text-xs">
            Set the scope and metadata, then open the Workbench to author the steps.
            {isMasterRepo && ' Need a new module, feature, release, or process? Add it inline below.'}
          </DialogDescription>
        </DialogHeader>

        <div className="space-y-3">
          {/* App (locked) */}
          <div className="space-y-1">
            <Label className="text-xs">Application</Label>
            <div>
              <Badge variant="secondary" className="text-xs px-2 py-0.5">{app.name}</Badge>
            </div>
          </div>

          {/* Name */}
          <div className="space-y-1">
            <Label className="text-xs" htmlFor="ntc-name">Test case name<span className="text-destructive">*</span></Label>
            <Input
              id="ntc-name"
              autoFocus
              value={name}
              onChange={(e) => setName(e.target.value)}
              placeholder="e.g. Submit purchase requisition"
              className="h-8 text-xs"
            />
          </div>

          {/* Module + Feature */}
          <div className="grid grid-cols-2 gap-3">
            {/* Module */}
            <div className="space-y-1">
              <Label className="text-xs">Module<span className="text-destructive">*</span></Label>
              {newModuleDraft === null ? (
               <Select
  value={moduleId}
  onValueChange={(v) => {
    if (v === NEW_SENTINEL) {
      setNewModuleDraft("");
    } else {
      setModuleId(v);
    }
  }}
  onOpenChange={(open) => {
    if (open) {
      fetchModulesForApp(
        "1eef4151-3303-4e6b-b784-0f43a24e2b00"
      );
    }
  }}
>
  <SelectTrigger className="h-8 text-xs">
    <SelectValue placeholder="Select module" />
  </SelectTrigger>

  <SelectContent>
    {apiModules.map((module) => (
      <SelectItem
        key={module.id}
        value={module.id}
        className="text-xs"
      >
        {module.name}
      </SelectItem>
    ))}

    {/* keep new module button */}
    <div className="my-1 border-t" />

    <SelectItem value={NEW_SENTINEL}>
      + Create New Module
    </SelectItem>
  </SelectContent>
</Select>
              ) : (
                <InlineCreateRow
                  placeholder="New module name"
                  value={newModuleDraft}
                  onChange={setNewModuleDraft}
                  onSave={handleCreateModule}
                  onCancel={() => setNewModuleDraft(null)}
                />
              )}
            </div>

            {/* Feature */}
            <div className="space-y-1">
              <Label className="text-xs">Feature<span className="text-destructive">*</span></Label>
              {newFeatureDraft === null ? (
                <Select
                  value={featureId}
                  onValueChange={(v) => v === NEW_SENTINEL ? setNewFeatureDraft('') : setFeatureId(v)}
                  disabled={!moduleId}
                >
                  <SelectTrigger className="h-8 text-xs"><SelectValue placeholder={moduleId ? 'Select feature' : 'Pick module first'} /></SelectTrigger>
                  <SelectContent>
                    {moduleFeatures.map(f => (
                      <SelectItem key={f.id} value={f.id} className="text-xs">{f.name}</SelectItem>
                    ))}
                    {moduleFeatures.length === 0 && (
                      <div className="px-2 py-1.5 text-[11px] text-muted-foreground">No features yet — add one.</div>
                    )}
                    {isMasterRepo && moduleId && (
                      <>
                        <SelectSeparator />
                        <SelectItem value={NEW_SENTINEL} className="text-xs text-primary">
                          <span className="inline-flex items-center gap-1"><PlusCircle className="h-3 w-3" /> New feature…</span>
                        </SelectItem>
                      </>
                    )}
                  </SelectContent>
                </Select>
              ) : (
                <InlineCreateRow
                  placeholder="New feature name"
                  value={newFeatureDraft}
                  onChange={setNewFeatureDraft}
                  onSave={handleCreateFeature}
                  onCancel={() => setNewFeatureDraft(null)}
                />
              )}
            </div>
          </div>

          {/* Role */}
          <div className="space-y-1">
            <Label className="text-xs" htmlFor="ntc-role">Role</Label>
            <Input
              id="ntc-role"
              value={role}
              onChange={(e) => setRole(e.target.value)}
              placeholder="e.g. Buyer"
              className="h-8 text-xs"
            />
          </div>

          {/* Releases / Processes */}
          <div className="grid grid-cols-2 gap-3">
            <MultiSelect
              label="Releases"
              options={appReleases.map(r => ({ id: r.id, label: r.name }))}
              selected={releaseIds}
              onChange={setReleaseIds}
              createForm={isMasterRepo ? {
                placeholder: 'Release name (e.g. R13 26C)',
                secondaryPlaceholder: 'Version (optional)',
                onCreate: (n, v) => !!handleCreateRelease(n, v ?? n),
              } : undefined}
            />
            <MultiSelect
              label="Processes"
              options={appProcesses.map(p => ({ id: p.id, label: p.name }))}
              selected={processIds}
              onChange={setProcessIds}
              createForm={isMasterRepo ? {
                placeholder: 'Process name (e.g. Quote-to-Order)',
                onCreate: (n) => !!handleCreateProcess(n),
              } : undefined}
            />
          </div>

          {/* Labels with free-add */}
          <LabelMultiSelect labels={labels} onChange={setLabels} />

          {/* Status + Type */}
          <div className="grid grid-cols-2 gap-3">
            <div className="space-y-1">
              <Label className="text-xs">Status</Label>
              <Select value={status} onValueChange={(v) => setStatus(v as StatusValue)}>
                <SelectTrigger className="h-8 text-xs"><SelectValue /></SelectTrigger>
                <SelectContent>
                  {STATUS_OPTIONS.map(s => (
                    <SelectItem key={s.id} value={s.id} className="text-xs">{s.label}</SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>
            <div className="space-y-1">
              <Label className="text-xs">Type</Label>
              <Select value={type} onValueChange={(v) => setType(v as 'standard' | 'customized')}>
                <SelectTrigger className="h-8 text-xs"><SelectValue /></SelectTrigger>
                <SelectContent>
                  <SelectItem value="standard" className="text-xs">Standard</SelectItem>
                  <SelectItem value="customized" className="text-xs">Customized</SelectItem>
                </SelectContent>
              </Select>
            </div>
          </div>
        </div>

        <DialogFooter>
          <Button variant="ghost" size="sm" onClick={() => onOpenChange(false)}>Cancel</Button>
          <Button size="sm" disabled={!canSubmit} onClick={handleOpenWorkbench}>
            <Wrench className="h-3.5 w-3.5 mr-1" /> Open Workbench
          </Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  );
}

// ---------------------------------------------------------------------------
// Inline create row — shared between module/feature inline forms.
// ---------------------------------------------------------------------------
function InlineCreateRow({
  placeholder, value, onChange, onSave, onCancel,
}: {
  placeholder: string;
  value: string;
  onChange: (v: string) => void;
  onSave: () => void;
  onCancel: () => void;
}) {
  return (
    <div className="flex items-center gap-1">
      <Input
        autoFocus
        placeholder={placeholder}
        value={value}
        onChange={(e) => onChange(e.target.value)}
        onKeyDown={(e) => {
          if (e.key === 'Enter') { e.preventDefault(); onSave(); }
          if (e.key === 'Escape') { e.preventDefault(); onCancel(); }
        }}
        className="h-8 text-xs"
      />
      <Button type="button" size="sm" variant="default" className="h-8 px-2" onClick={onSave} disabled={!value.trim()}>
        <Check className="h-3.5 w-3.5" />
      </Button>
      <Button type="button" size="sm" variant="ghost" className="h-8 px-2" onClick={onCancel}>
        <X className="h-3.5 w-3.5" />
      </Button>
    </div>
  );
}

interface CreateForm {
  placeholder: string;
  secondaryPlaceholder?: string;
  /** Returns true if create succeeded so we can close the form. */
  onCreate: (name: string, secondary?: string) => boolean;
}

function MultiSelect({
  label, options, selected, onChange, createForm,
}: {
  label: string;
  options: Array<{ id: string; label: string }>;
  selected: string[];
  onChange: (next: string[]) => void;
  createForm?: CreateForm;
}) {
  const [creating, setCreating] = useState(false);
  const [draft1, setDraft1] = useState('');
  const [draft2, setDraft2] = useState('');

  const toggle = (id: string) =>
    onChange(selected.includes(id) ? selected.filter(x => x !== id) : [...selected, id]);

  const reset = () => { setCreating(false); setDraft1(''); setDraft2(''); };

  const submit = () => {
    if (!createForm || !draft1.trim()) return;
    const ok = createForm.onCreate(draft1.trim(), draft2.trim() || undefined);
    if (ok) reset();
  };

  return (
    <div className="space-y-1">
      <Label className="text-xs">{label}</Label>
      <Popover onOpenChange={(o) => { if (!o) reset(); }}>
        <PopoverTrigger asChild>
          <Button variant="outline" size="sm" className="h-8 w-full justify-between text-xs font-normal">
            <span className="truncate text-muted-foreground">
              {selected.length === 0 ? `Select ${label.toLowerCase()}` : `${selected.length} selected`}
            </span>
            <ChevronDown className="h-3 w-3 opacity-60" />
          </Button>
        </PopoverTrigger>
        <PopoverContent className="w-72 p-0" align="start">
          <Command>
            <CommandInput placeholder={`Search ${label.toLowerCase()}…`} className="text-xs" />
            <CommandList>
              <CommandEmpty>No matches</CommandEmpty>
              <CommandGroup>
                {options.map(opt => {
                  const checked = selected.includes(opt.id);
                  return (
                    <CommandItem key={opt.id} value={opt.label} onSelect={() => toggle(opt.id)} className="text-xs cursor-pointer">
                      <div className={cn(
                        'mr-2 h-3.5 w-3.5 rounded-sm border flex items-center justify-center',
                        checked ? 'bg-primary border-primary' : 'border-input',
                      )}>
                        {checked && <Check className="h-3 w-3 text-primary-foreground" />}
                      </div>
                      <span className="flex-1 truncate">{opt.label}</span>
                    </CommandItem>
                  );
                })}
              </CommandGroup>
            </CommandList>
          </Command>
          {createForm && (
            <div className="border-t p-2 space-y-1.5 bg-muted/30">
              {!creating ? (
                <Button
                  type="button" variant="ghost" size="sm"
                  className="w-full h-7 text-xs justify-start text-primary"
                  onClick={() => setCreating(true)}
                >
                  <PlusCircle className="h-3 w-3 mr-1" /> New {label.toLowerCase().replace(/s$/, '')}…
                </Button>
              ) : (
                <>
                  <Input
                    autoFocus
                    placeholder={createForm.placeholder}
                    value={draft1}
                    onChange={(e) => setDraft1(e.target.value)}
                    onKeyDown={(e) => {
                      if (e.key === 'Enter' && !createForm.secondaryPlaceholder) { e.preventDefault(); submit(); }
                      if (e.key === 'Escape') { e.preventDefault(); reset(); }
                    }}
                    className="h-7 text-xs"
                  />
                  {createForm.secondaryPlaceholder && (
                    <Input
                      placeholder={createForm.secondaryPlaceholder}
                      value={draft2}
                      onChange={(e) => setDraft2(e.target.value)}
                      onKeyDown={(e) => {
                        if (e.key === 'Enter') { e.preventDefault(); submit(); }
                        if (e.key === 'Escape') { e.preventDefault(); reset(); }
                      }}
                      className="h-7 text-xs"
                    />
                  )}
                  <div className="flex gap-1 justify-end">
                    <Button type="button" size="sm" variant="ghost" className="h-6 text-[11px] px-2" onClick={reset}>Cancel</Button>
                    <Button type="button" size="sm" className="h-6 text-[11px] px-2" disabled={!draft1.trim()} onClick={submit}>Create</Button>
                  </div>
                </>
              )}
            </div>
          )}
        </PopoverContent>
      </Popover>
      {selected.length > 0 && (
        <div className="flex flex-wrap gap-1 pt-1">
          {selected.map(id => {
            const opt = options.find(o => o.id === id);
            return (
              <Badge key={id} variant="secondary" className="text-[10px] gap-1 pr-1">
                {opt?.label ?? id}
                <button onClick={() => toggle(id)} className="hover:text-foreground" aria-label="Remove">
                  <X className="h-2.5 w-2.5" />
                </button>
              </Badge>
            );
          })}
        </div>
      )}
    </div>
  );
}

function LabelMultiSelect({ labels, onChange }: { labels: string[]; onChange: (next: string[]) => void }) {
  const [draft, setDraft] = useState('');
  const toggle = (l: string) =>
    onChange(labels.includes(l) ? labels.filter(x => x !== l) : [...labels, l]);
  const addNew = () => {
    const v = draft.trim();
    if (!v) return;
    if (!labels.includes(v)) onChange([...labels, v]);
    addLabelToVocabulary(v);
    setDraft('');
  };
  const suggestions = labelVocabulary.filter(l => !labels.includes(l));
  return (
    <div className="space-y-1">
      <Label className="text-xs">Labels</Label>
      <div className="flex gap-1">
        <Input
          value={draft}
          onChange={(e) => setDraft(e.target.value)}
          onKeyDown={(e) => { if (e.key === 'Enter') { e.preventDefault(); addNew(); } }}
          placeholder="Add a label and press Enter"
          className="h-8 text-xs"
        />
        <Button type="button" variant="outline" size="sm" className="h-8" onClick={addNew} disabled={!draft.trim()}>
          <Plus className="h-3.5 w-3.5" />
        </Button>
      </div>
      {(labels.length > 0 || suggestions.length > 0) && (
        <div className="flex flex-wrap gap-1 pt-1">
          {labels.map(l => (
            <Badge key={`sel-${l}`} variant="default" className="text-[10px] gap-1 pr-1">
              {l}
              <button onClick={() => toggle(l)} className="hover:text-foreground" aria-label="Remove">
                <X className="h-2.5 w-2.5" />
              </button>
            </Badge>
          ))}
          {suggestions.slice(0, 6).map(l => (
            <button
              key={`sug-${l}`}
              onClick={() => toggle(l)}
              className="text-[10px] px-1.5 py-0.5 rounded border border-dashed border-muted-foreground/40 text-muted-foreground hover:bg-secondary"
            >
              + {l}
            </button>
          ))}
        </div>
      )}
    </div>
  );
}
